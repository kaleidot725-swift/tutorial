import Foundation


func no8() {
    // クラス名や型名などは UpperCamelCase を使う
    class Hoge {}
    
    // メソッド名や変数名などは LowerCamelCase を使う
    let message = "Sample"
    print(message)
    
    // 予約語を使いたいときはシングルクォーテーションで囲む
    let `fallthrough` = 10
    print(`fallthrough`)
}
